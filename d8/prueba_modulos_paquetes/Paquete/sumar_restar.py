# Un módulo en términos simples es un archivo .py cualquiera
# y un paquete es un conjunto de módulos. Opcionalmente, también
# puede contener subpaquetes.
#
# El archivo __init__.py (puede estar vacío) le indica a Python que
# allí donde se encuentra dicho archivo es un paquete.


def sumar(num1, num2):
    print(num1 + num2)


def restar(num1, num2):
    print(num1 - num2)
